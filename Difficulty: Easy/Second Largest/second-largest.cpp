// User function template for C++
class Solution {
  public:
    // Function returns the second
    // largest elements
    int print2largest(vector<int> &arr) {
        // Code Here
        int maxi=0;
        for(auto i:arr){
            
            maxi=max(maxi,i);
        }
        int smax=-1;
        for(auto i:arr){
            
            if(i<maxi && i>smax){
                smax=i;
            }
            
        }
        return smax;
    }
};

